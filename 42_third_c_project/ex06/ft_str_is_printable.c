int	ft_str_is_printable(char *str)
{
	int	i;
	int	rvalue;

	i = 0;
	rvalue = 1;
	while (str[i] != '\0')
	{
		if (str[i] <= 31 || str[i] == 127)
		{
			rvalue = 0;
		}
		i++;
	}
	return (rvalue);
}
